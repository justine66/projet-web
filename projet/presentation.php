			<p>L’article 2 des statuts de l’association qui organise et dirige Théâtres de Bourbon précise que la programmation visera à proposer du théâtre qui fait sens dans des lieux qui font sens. L’association privilégiera les spectacles qui nourrissent une réflexion sur le temps présent et offrent un miroir à notre humanité pour reprendre la formule de Hamlet. Il ajoute que le festival se mettra également au service des troupes programmées, et se voudra un instrument de mise en valeur de leur travail. Il aura pour objet d’encourager la création théâtrale contemporaine, et, autant que possible, d’aider les jeunes troupes, les jeunes auteurs dramatiques et les jeunes metteurs en scène. 
