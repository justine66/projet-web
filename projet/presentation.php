						<h1>Le festival</h1>	
					
						<p>
							Ce festival se présente comme une rencontre conviviale entre amoureux du théâtre et amateurs curieux. 
							Les spectacles proposés sont engagés à changer la société en posant des questions qui émeuvent et qui bouleversent. 
							Jean Luc Jeener, notre parrain pour cette aventure, dit que “faire un théâtre sans que l’homme soit au coeur du projet est vain”. 
							Sur de telles bases, notre objectif n’est rien moins que créer entre troupes et spectateurs une dynamique qui change 
							quelque chose dans notre vie à tous! 
							Et parce que le beau est la meilleure façon de toucher les âmes, 
							ce festival s’enracine dans un terroir au cœur de la France, le Bourbonnais, 
							et se déroule exclusivement dans des lieux d’exception, classés, qui sont des écrins propres à sublimer les textes que nous vous proposons.
							Les troupes participantes sont toutes aguerries et professionnelles, et partag&#8239;ent avec nous une approche passionnée du théâtre, elles ont hâte de vous faire découvrir les œuvres sélectionnées par notre directeur artistique.
							N’hésitez pas à vivre avec nous cette expérience unique, comme spectateur, ou, pourquoi pas, comme membre de l’association&#8239;! 
						</p>
					
